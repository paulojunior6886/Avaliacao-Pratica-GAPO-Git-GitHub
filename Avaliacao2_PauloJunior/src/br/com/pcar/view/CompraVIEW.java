    private void preencherDetalhesCompra() {
        if (clienteLogadoDTO != null && veiculoParaComprarDTO != null) {
            lblNomeClienteCompra.setText(clienteLogadoDTO.getNome_usuario());
            lblVeiculoMarcaModelo.setText(veiculoParaComprarDTO.getMarca_veiculo() + " " + veiculoParaComprarDTO.getModelo_veiculo());
            lblVeiculoAno.setText(String.valueOf(veiculoParaComprarDTO.getAno_modelo_veiculo()));
            lblVeiculoCor.setText(veiculoParaComprarDTO.getCor_veiculo());
            lblVeiculoPlaca.setText(veiculoParaComprarDTO.getPlaca_veiculo());
            
            // Formatar preço para moeda local
            String precoFormatado = String.format(new Locale("pt", "BR"), "R$ %,.2f", veiculoParaComprarDTO.getPreco_veiculo());
            lblValorCompra.setText(precoFormatado);
            
            // Data atual para a venda
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy"); // Formato de exibição
            lblDataCompra.setText(sdf.format(new Date()));
        } else {
            JOptionPane.showMessageDialog(this, "Erro: Dados do cliente ou do veículo não foram recebidos corretamente.", "Erro de Inicialização", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }
    

    private void btnConfirmarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarCompraActionPerformed
        if (clienteLogadoDTO == null || veiculoParaComprarDTO == null) {
            JOptionPane.showMessageDialog(this, "Erro: Dados do cliente ou veículo não disponíveis.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 1. Obter o ID do cliente da tabela 'clientes' usando o CPF do UsuarioDTO
        String cpfCliente = clienteLogadoDTO.getCpf_usuario();
System.out.println("DEBUG CompraVIEW: CPF sendo usado para busca: '" + cpfCliente + "'"); 
        if (cpfCliente == null || cpfCliente.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Erro: CPF do cliente não encontrado para registrar a venda.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int idClienteTabela = clienteCTR.buscarIdClientePorCPF(cpfCliente); // Usa o novo método
        
        if (idClienteTabela == -1) {
            JOptionPane.showMessageDialog(this, "Erro: Cliente não encontrado no cadastro de clientes com o CPF: " + cpfCliente, "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        PcarDTO novaVendaDTO = new PcarDTO(); //
        novaVendaDTO.setId_cliente(idClienteTabela); // ID da tabela clientes
        novaVendaDTO.setId_veiculo(veiculoParaComprarDTO.getId_veiculo()); //
        
        // Data da Venda no formato YYYY-MM-DD
        SimpleDateFormat sdfDb = new SimpleDateFormat("yyyy-MM-dd");
        novaVendaDTO.setData_venda(sdfDb.format(new Date())); //
        
        novaVendaDTO.setValor_venda(veiculoParaComprarDTO.getPreco_veiculo()); //
        novaVendaDTO.setStatus_venda("Pendente"); // Status inicial

        String resultado = vendaCTR.inserirVenda(novaVendaDTO); //
        JOptionPane.showMessageDialog(this, resultado, "Status da Compra", JOptionPane.INFORMATION_MESSAGE);

        if (resultado.toLowerCase().contains("sucesso")) {
            this.compraRealizada = true;
            // Opcional: Marcar o veículo como "Vendido" ou "Reservado" no banco de dados
            // Isso exigiria um método em VeiculoCTR/DAO para atualizar o status do veículo.
            // Ex: veiculoCTR.atualizarStatusVeiculo(veiculoParaComprarDTO.getId_veiculo(), "Vendido");
            this.dispose(); // Fecha o diálogo de compra
        }
    }//GEN-LAST:event_btnConfirmarCompraActionPerformed

